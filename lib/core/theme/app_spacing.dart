// ---------------------------------------------------------------------------
// AppSpacing — 8pt grid scale used across the redesign.
// Always reference these named tokens instead of literal pixel values.
//   xs  → 4   (icon padding, tight chip gutters)
//   sm  → 8   (chip → chip, icon → text)
//   md  → 12  (default card inner padding)
//   lg  → 16  (default screen edge padding)
//   xl  → 24  (section gaps)
//   xxl → 32  (hero spacing)
//   xxxl→ 48  (top-of-screen breathing room)
//   huge→ 64  (splash + onboarding hero blocks)
// ---------------------------------------------------------------------------

abstract final class AppSpacing {
  static const double xs   = 4;
  static const double sm   = 8;
  static const double md   = 12;
  static const double lg   = 16;
  static const double xl   = 24;
  static const double xxl  = 32;
  static const double xxxl = 48;
  static const double huge = 64;
}
